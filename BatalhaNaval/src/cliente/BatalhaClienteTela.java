        private void enviaRetornoAcertoErro(String ss) {
            try {  
                out = new DataOutputStream(socket.getOutputStream());
            } catch (IOException ex) {
                Logger.getLogger(BatalhaClienteTela.class.getName()).log(Level.SEVERE, null, ex);
            }

            try {
                out.writeUTF(ss);
            } catch (IOException ex) {
                Logger.getLogger(BatalhaClienteTela.class.getName()).log(Level.SEVERE, null, ex);
            }
            try {
                out.flush();
            } catch (IOException ex) {
                Logger.getLogger(BatalhaClienteTela.class.getName()).log(Level.SEVERE, null, ex);
            }
        }

